//IndexOf - primeira posição do valor
//LastIndexOf - ultima posição do valor

num = [5,6,7,8,4,4,4];

console.log(num.indexOf(4));
//traz o número 4 por estar na quarta posição - primeira posição que se encontra o número 4
//caso 8 - traz o 3  por estar na terceira posição

console.log(num.lastIndexOf(4));

console.log(num.indexOf(3));